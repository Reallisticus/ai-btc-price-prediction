import { NextPage } from "next";
import Head from "next/head";
import { useEffect, useState } from "react";
import Typing from "~/components/typing.component";
import { env } from "~/env.mjs";

type GptResponse = {
  text: string;
  index: number;
  logprobs: null;
  finish_reason: string;
};

const Home: NextPage = () => {
  const [response, setResponse] = useState<GptResponse>();
  const [prompt, setPrompt] = useState("");

  const handleInputChange = (event: React.KeyboardEvent<HTMLInputElement>) => {
    if (event.key === "Enter") {
      if (prompt !== "") {
        fetch(`http://localhost:3000/api/openai?prompt=${prompt}`)
          .then((res) => res.json())
          .then((data: GptResponse) => setResponse(data))
          .catch((err) => {
            console.log(err);
          });
      }
    }
  };

  useEffect(() => {
    console.log(response);
  }, [response && response.text]);

  return (
    <>
      <Head>
        <meta name="description" content="Generated by create-t3-app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main className="flex min-h-screen flex-col items-center justify-center bg-gradient-to-b from-[#2e026d] to-[#15162c]">
        <div className="w-3/4">
          <input
            type="text"
            className="w-full rounded p-2"
            placeholder="Enter your prompt"
            value={prompt}
            onChange={(e) => setPrompt(e.target.value)}
            onKeyDown={handleInputChange}
          />
          {response && (
            <div className="mt-4 rounded bg-gray-100 p-4">
              <Typing key={response.index} text={response.text} />
            </div>
          )}
        </div>
      </main>
    </>
  );
};

export default Home;
